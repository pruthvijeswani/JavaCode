//Assignment 2: Declare and initilize the variables for all datatypes

package java_assignments;

public class AllVariables_Assignment2
{
	public static void main(String[] args) 
	{
		byte x = 25;
		System.out.println(x);
		
		short y = 32000;
		System.out.println(y);
		
		int z = 450000;
		System.out.println(z);
		
		long mobileNumber = 9876543210L;
		System.out.println(mobileNumber);
		
		float percentage = 82.75f;
		System.out.println(percentage);
		
		double salary = 45678.9876;
		System.out.println(salary);
		
		char grade = 'A';
		System.out.println(grade);
		
		boolean isPassed = true;
		System.out.println(isPassed);
		
		boolean isAbsent = false;
		System.out.println(isAbsent);
		
		String city = "My city is XYZ";
		System.out.println(city);
	}
}
